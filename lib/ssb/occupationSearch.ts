function normalize(text: string) {
  return text
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[-–—]/g, ' ')
    .replace(/[()]/g, ' ')
    .replace(/[^\p{L}\p{N}/, ]/gu, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function noAsciiVariants(s: string): string[] {
  const lower = s.toLowerCase()
  const ascii = NO_ASCII_MAP.reduce((acc, [re, rep]) => acc.replace(re, rep), lower)
  return Array.from(new Set([lower, ascii]))
}

function addSimpleMorphology(words: string[]): string[] {
  const out = new Set(words)
  for (const w of words) {
    if (w.length <= 3) continue
    if (w.endsWith('ene')) out.add(w.slice(0, -3))
    if (w.endsWith('ende')) out.add(w.slice(0, -4))
    if (w.endsWith('ere')) out.add(w.slice(0, -3))
    if (w.endsWith('er')) out.add(w.slice(0, -2))
    if (w.endsWith('e')) out.add(w.slice(0, -1))
    if (w.endsWith('ing')) out.add(w.slice(0, -3))
    if (w.endsWith('s')) out.add(w.slice(0, -1))
  }
  return [...out]
}

function tokensAndPhrases(label: string): { tokens: string[]; phrases: string[] } {
  const cleaned = normalize(label)
    .replace(/\b(mv|m\.v|ekskl|unntatt)\b\.?/g, '')
    .trim()
  const phrases = cleaned
    .split(/[/,]/g)
    .map(p => p.trim())
    .filter(Boolean)

  const tokens = phrases
    .flatMap(p => p.split(' '))
    .map(t => t.trim())
    .filter(t => t && !STOP_WORDS.has(t))

  return {
    tokens: Array.from(new Set(tokens)),
    phrases: Array.from(new Set(phrases)),
  }
}

function expandByLexicon(items: string[]): string[] {
  const out = new Set(items)
  for (const s of items) {
    for (const [re, syns] of NO_LEXICON) {
      if (re.test(s)) syns.forEach(x => out.add(x))
    }
  }
  return [...out]
}

function buildAliases(code: string, label: string): string[] {
  const { tokens, phrases } = tokensAndPhrases(label)

  const tokenExpanded = addSimpleMorphology(tokens)
  const phraseExpanded = Array.from(
    new Set([
      ...phrases,
      ...phrases.map(p => p.replace(/\s+/g, '-')),
      ...phrases.map(p => p.replace(/\s+/g, '')),
    ]),
  )

  const asciiTokens = tokenExpanded.flatMap(t => noAsciiVariants(t))
  const asciiPhrases = phraseExpanded.flatMap(p => noAsciiVariants(p))

  const base = expandByLexicon([...asciiTokens, ...asciiPhrases])

  const codeSpaced = code.split('').join(' ')
  return Array.from(new Set([...base, code, codeSpaced]))
}

export function toOccupationDocs(map: Record<string, string>): SsbOccupationDoc[] {
  return Object.entries(map).map(([code, label]) => ({
    code,
    label,
    aliases: buildAliases(code, label),
  }))
}

export function createOccupationFuse(docs: SsbOccupationDoc[]) {
  return new Fuse(docs, {
    includeScore: true,
    threshold: 0.35,
    ignoreLocation: true,
    useExtendedSearch: true,
    keys: [
      { name: 'code', weight: 3 },
      { name: 'label', weight: 2 },
      { name: 'aliases', weight: 1 },
    ],
  })
}

export function searchSsbOccupations(
  fuse: Fuse<SsbOccupationDoc>,
  query: string,
  limit = 8,
): Array<SsbOccupationDoc & { score: number }> {
  const q = query.trim()
  if (!q) return []

  const digits = q.replace(/\s+/g, '')
  const isCodeQuery = /^\d+$/.test(digits)

  const fuseQuery: string | Expression = isCodeQuery
    ? { $or: [{ code: `=${digits}` }, { aliases: `=${digits}` }, { code: digits }] }
    : normalize(q)

  return fuse.search(fuseQuery, { limit }).map(r => ({
    ...r.item,
    score: r.score ?? 1,
  }))
}

