const STOP_WORDS = new Set([
  'mv',
  'm.v',
  'ekskl',
  'unntatt',
  'andre',
  'innen',
  'av',
  'og',
  'i',
  'for',
  'med',
  'til',
  'ved',
  'på',
])

const NO_ASCII_MAP: Array<[RegExp, string]> = [
  [/æ/g, 'ae'],
  [/ø/g, 'oe'],
  [/å/g, 'aa'],
]

